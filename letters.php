function print_letters($letters) {

    foreach ($letters as $letter) {

        echo PHP_EOL;

        foreach ($letter as $line) {
            $pixels = str_split($line);
            foreach ($pixels as $pixel) {
                echo ($pixel=="1") ? "■" : " ";
            }
            echo PHP_EOL;
        }

